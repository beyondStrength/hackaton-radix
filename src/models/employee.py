class Employee:
    def __init__(self, name, age, role):
        self.name = name
        self.age = age
        self.role = role


class EmployeeVitals:
    def __init__(self, heart_rate, blood_presure_high, blood_presure_low, datetime, sleep):
        self.heart_rate = heart_rate
        self.blood_presure_high = blood_presure_high
        self.blood_presure_low = blood_presure_low
        self.sleep = sleep
        self.datetime = datetime

